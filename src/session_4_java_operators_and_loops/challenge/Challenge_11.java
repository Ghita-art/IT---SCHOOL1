package session_4_java_operators_and_loops.challenge;

public class Challenge_11 { // Write a Java program where you declare four integer variables: a, b, c, and d.
    // Assign them values of 10, 20, 30, and 40 respectively. Create two more variables additionResult and multiplicationResult.
    // Set additionResult to the sum of a and b and multiplicationResult to the product of c and d.
    // Print the values of additionResult and multiplicationResult.
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 30;
        int d = 40;
        double addition = a + b;
        double multiplication = c * d;
        System.out.println("Addition result: " + addition);
        System.out.println("Multiplication result: " + multiplication);
    }
}
