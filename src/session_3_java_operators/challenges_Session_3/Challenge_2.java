package session_3_java_operators.challenges_Session_3;

public class Challenge_2 {
    public static void main(String[] args) {
        int counter = 10;
        System.out.println(counter);
        System.out.println(++counter);
        System.out.println(counter);
        System.out.println(counter--);
        System.out.println(counter);
    }
}

