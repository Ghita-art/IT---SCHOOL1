package session_3_java_operators.challenges_Session_3;

public class Challenge_1 {

        public static void main(String[] args) {
            int number1 = 200;
            int number2 = 10;
            System.out.println(number1 + " + " + number2 + " = " + (number1 + number2));
            System.out.println(number1 + " - " + number2 + " = " + (number1 - number2));
            System.out.println(number1 + " * " + number2 + " = " + (number1 * number2));
            System.out.println(number1 + " / " + number2 + " = " + (number1 / number2));
            System.out.println(number1 + " % " + number2 + " = " + (number1 % number2));
        }
    }

